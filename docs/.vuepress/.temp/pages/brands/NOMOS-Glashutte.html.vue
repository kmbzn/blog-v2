<p><img src="https://upload.wikimedia.org/wikipedia/commons/2/24/NOMOS_Glashuette_logo.png" alt="NOMOS Glashütte Logo"></p>
<p>NOMOS Glashütte는 독일 작센주 글라스휘테(Glashütte)에 본사를 둔 독립 시계 브랜드다. 1990년 독일 통일 직후 설립되었으며, 바우하우스(Bauhaus) 미학을 기반으로 한 절제된 디자인과 자체 제작 무브먼트로 전 세계 시계 애호가들에게 높은 평가를 받고 있다.</p>
<h2 id="브랜드-개요" tabindex="-1"><a class="header-anchor" href="#브랜드-개요"><span>브랜드 개요</span></a></h2>
<p>글라스휘테는 19세기부터 독일 시계 산업의 심장부였다. A. Lange &amp; Söhne를 비롯한 명문 시계 공방들이 이 작은 마을에 뿌리를 두고 있었지만, 동독 시절 국유화를 거치며 그 전통이 단절되었다. NOMOS는 통일 후 이 유산을 이어받아 새롭게 출발한 브랜드로, 설립 초기부터 외부 무브먼트 의존을 최소화하고 자체 칼리버 개발에 집중했다.</p>
<p>디자인 언어는 일관되게 바우하우스 원칙에 충실하다. 불필요한 장식을 걷어내고 기능과 형태가 일치하는 구성, 흰색 다이얼과 블루 핸즈의 조화, 그리고 뛰어난 가독성이 NOMOS 시계를 한눈에 알아볼 수 있게 한다.</p>
<h2 id="주요-컬렉션" tabindex="-1"><a class="header-anchor" href="#주요-컬렉션"><span>주요 컬렉션</span></a></h2>
<h3 id="tangente" tabindex="-1"><a class="header-anchor" href="#tangente"><span>Tangente</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/2/2b/NOMOS_Glashütte_Tangente.jpg" alt="NOMOS Glashütte Tangente"></p>
<p>탕겐테는 NOMOS의 상징이자 가장 오래된 모델이다. 1992년 처음 선보인 이래 브랜드의 정체성 그 자체로 자리잡았다. 얇은 케이스, 섬세한 바우하우스 인덱스, 오프셋 세컨즈(auxiliary seconds)가 특징이며, 어떤 손목에도 과하지 않게 어울린다.</p>
<ul>
<li>케이스 직경: 33mm / 35mm / 38mm</li>
<li>두께: 약 6.4mm</li>
<li>무브먼트: Alpha (수동감기)</li>
<li>특징: 6시 방향 보조 초침</li>
</ul>
<h3 id="tangente-neomatik" tabindex="-1"><a class="header-anchor" href="#tangente-neomatik"><span>Tangente neomatik</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/7/70/Nomos_Tangente_neomatik.jpg" alt="NOMOS Tangente neomatik"></p>
<p>탕겐테의 자동감기 버전. NOMOS가 자체 개발한 DUW 3001 자동 무브먼트를 탑재하여 얇은 두께를 유지하면서도 편의성을 더했다. 스윙 시스템(Swing System)이라 불리는 독자적인 로터 구조 덕분에 두께가 7.5mm에 불과하다.</p>
<h3 id="ludwig" tabindex="-1"><a class="header-anchor" href="#ludwig"><span>Ludwig</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/c/c0/NOMOS_Ludwig_Vorderseite.jpg" alt="NOMOS Glashütte Ludwig"></p>
<p>루트비히는 탕겐테의 각진 케이스와 달리 원형 케이스를 채택한 모델이다. 보다 클래식한 느낌을 원하는 이들에게 적합하며, 3시 방향 소창(small seconds)이 탕겐테와 다른 개성을 만든다. 드레스워치로서의 완성도가 높다.</p>
<ul>
<li>케이스 직경: 35mm / 38mm</li>
<li>무브먼트: Alpha (수동감기) / DUW 3001 (자동감기)</li>
<li>특징: 3시 방향 소초침</li>
</ul>
<h3 id="orion" tabindex="-1"><a class="header-anchor" href="#orion"><span>Orion</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/c/c7/NOMOS_Glashütte_Orion.jpg" alt="NOMOS Glashütte Orion"></p>
<p>오리온은 NOMOS 라인업 중 가장 스포티한 감각을 가진 모델이다. 슈퍼루미노바 코팅 핸즈와 인덱스로 야광 성능을 갖췄으며, 루트비히와 마찬가지로 원형 케이스이지만 보다 실용적인 성격이 강하다. 일상 착용에 무리 없는 내구성과 편의성을 갖추고 있다.</p>
<ul>
<li>케이스 직경: 36mm / 38mm / 41mm</li>
<li>무브먼트: Alpha (수동감기) / DUW 3001 (자동감기)</li>
<li>방수: 10m</li>
</ul>
<h3 id="club" tabindex="-1"><a class="header-anchor" href="#club"><span>Club</span></a></h3>
<p>클럽은 NOMOS 라인업 중 가장 캐주얼하고 접근하기 쉬운 모델로, 입문자에게 권장되는 시작점이다. 두꺼운 케이스 러그와 가죽 혹은 NATO 스트랩 조합이 잘 어울리며, NOMOS의 시계 철학을 가장 낮은 가격에 경험할 수 있다.</p>
<ul>
<li>케이스 직경: 36mm / 38mm / 42mm</li>
