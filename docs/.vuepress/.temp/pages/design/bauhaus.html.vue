<p>현대 디자인에서 &quot;바우하우스적&quot;이라는 수식어는 절제, 기능성, 기하학적 형태, 장식의 배제를 의미한다. NOMOS 시계, Apple의 제품 디자인, Dieter Rams의 브라운(Braun) 제품들, 그리고 스위스 그래픽 디자인의 계보 모두 바우하우스의 직계 후손이다.</p>
<h2 id="바우하우스와-현재" tabindex="-1"><a class="header-anchor" href="#바우하우스와-현재"><span>바우하우스와 현재</span></a></h2>
<p>바우하우스의 원칙은 디지털 세계에서도 살아 있다. Google의 Material Design, Apple의 Human Interface Guidelines, 스칸디나비아 미니멀리즘 인테리어 등 모두 &quot;형태는 기능을 따른다&quot;는 원칙 위에 있다.</p>
<p>설립 100주년인 2019년에는 데사우·베를린·바이마르 세 도시에서 대규모 기념 행사와 전시가 열렸고, 유네스코와 독일 정부가 공동으로 바우하우스 유산의 디지털 아카이브화 프로젝트를 진행했다.</p>
<p>1933년에 문을 닫은 학교가 21세기에도 이렇게 참조된다는 사실 자체가, 바우하우스가 단순한 디자인 사조가 아니라 하나의 <strong>사유 방식</strong>임을 방증한다.</p>
