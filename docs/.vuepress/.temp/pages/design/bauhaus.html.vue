<template><div><h1 id="바우하우스-현대-디자인의-원점" tabindex="-1"><a class="header-anchor" href="#바우하우스-현대-디자인의-원점"><span>바우하우스: 현대 디자인의 원점</span></a></h1>
<DateMeta />
<p><img src="https://upload.wikimedia.org/wikipedia/commons/9/98/Dessau_Bauhaus_neu.JPG" alt="바우하우스 데사우 건물"></p>
<p>바우하우스(Bauhaus)는 1919년 독일 바이마르(Weimar)에서 발터 그로피우스(Walter Gropius)가 설립한 예술·공예·건축 학교다. 단 14년간 존재했지만, 현대 디자인·건축·타이포그래피·가구·사진 등 거의 모든 시각 문화 분야에 걸쳐 지금까지도 가장 강력하게 작동하는 영향력을 남겼다. &quot;Bauhaus&quot;는 독일어로 &quot;건축의 집(Haus des Bauens)&quot;을 의미한다.</p>
<h2 id="설립과-역사" tabindex="-1"><a class="header-anchor" href="#설립과-역사"><span>설립과 역사</span></a></h2>
<h3 id="바이마르-시대-1919–1925" tabindex="-1"><a class="header-anchor" href="#바이마르-시대-1919–1925"><span>바이마르 시대 (1919–1925)</span></a></h3>
<p>1919년 4월, 그로피우스는 바이마르의 두 학교—그랑두칼 작센 예술학교와 공예학교—를 통합해 바우하우스를 설립했다. 창립 선언문에서 그는 이렇게 썼다.</p>
<blockquote>
<p>&quot;모든 창조적 활동의 궁극적 목표는 건축이다. 건축을 장식하는 것은 조각가와 화가의 특권이었지만, 그것은 예술이 고립된 살롱 예술이 되기 이전의 일이었다. 오래된 예술학교는 이 통합을 가르칠 수 없었다.&quot;</p>
</blockquote>
<p>초기 바이마르 시대에는 표현주의와 낭만주의적 성격이 강했다. 각 공방은 예술가 마이스터(Formmeister)와 장인 마이스터(Werkmeister) 두 명이 함께 지도했다. 파울 클레(Paul Klee), 바실리 칸딘스키(Wassily Kandinsky), 라슬로 모홀리-나기(László Moholy-Nagy) 같은 거장들이 교수진이었다.</p>
<h3 id="데사우-시대-1925–1932" tabindex="-1"><a class="header-anchor" href="#데사우-시대-1925–1932"><span>데사우 시대 (1925–1932)</span></a></h3>
<p>1925년, 정치적 압박으로 바이마르를 떠나 데사우(Dessau)로 이전했다. 이 시기가 바우하우스의 황금기다. 그로피우스가 직접 설계한 새 캠퍼스 건물은 그 자체로 바우하우스 원칙의 구현이었으며, 지금도 UNESCO 세계문화유산으로 지정되어 있다.</p>
<p>데사우에서 바우하우스는 더 산업적이고 합리적인 방향으로 전환했다. &quot;예술과 기술의 통합&quot;이 핵심 슬로건이 되었고, 대량생산 가능한 디자인이 중심 과제가 되었다.</p>
<p>1928년 그로피우스가 사임하고 하네스 마이어(Hannes Meyer)가 뒤를 이었으며, 1930년에는 미스 반 데어 로에(Mies van der Rohe)가 교장을 맡았다.</p>
<h3 id="베를린-시대와-폐교-1932–1933" tabindex="-1"><a class="header-anchor" href="#베를린-시대와-폐교-1932–1933"><span>베를린 시대와 폐교 (1932–1933)</span></a></h3>
<p>나치당의 지속적인 압박으로 데사우를 떠나 베를린으로 이전했으나, 1933년 7월 나치 정권에 의해 강제 폐교되었다. 많은 교수와 학생들이 미국·이스라엘·영국 등으로 망명하며 바우하우스의 사상을 전 세계로 확산시켰다.</p>
<h2 id="핵심-원칙" tabindex="-1"><a class="header-anchor" href="#핵심-원칙"><span>핵심 원칙</span></a></h2>
<h3 id="예술과-공예의-통합" tabindex="-1"><a class="header-anchor" href="#예술과-공예의-통합"><span>예술과 공예의 통합</span></a></h3>
<p>바우하우스 이전까지 순수예술(fine art)과 응용예술(applied art·공예)은 엄격히 위계화되어 있었다. 그로피우스는 이 구분을 해체하고자 했다. 도예·직조·금속·가구·타이포그래피·무대 디자인 모두 동등한 예술적 탐구의 장이었다.</p>
<h3 id="형태는-기능을-따른다-form-follows-function" tabindex="-1"><a class="header-anchor" href="#형태는-기능을-따른다-form-follows-function"><span>형태는 기능을 따른다 (Form follows function)</span></a></h3>
<p>장식을 위한 장식은 거부했다. 물체의 형태는 그것이 수행하는 기능으로부터 자연스럽게 도출되어야 한다. 이 원칙은 오늘날 모든 제품 디자인·UI 디자인·건축의 기본 명제로 남아 있다.</p>
<h3 id="대량생산과의-화해" tabindex="-1"><a class="header-anchor" href="#대량생산과의-화해"><span>대량생산과의 화해</span></a></h3>
<p>수공예의 아름다움을 보존하면서도, 기계 생산과 화해하는 것이 바우하우스의 과제였다. 최종 목표는 더 많은 사람이 아름다운 물건을 쓸 수 있는 세상이었다.</p>
<h3 id="게잠트쿤스트베르크-gesamtkunstwerk" tabindex="-1"><a class="header-anchor" href="#게잠트쿤스트베르크-gesamtkunstwerk"><span>게잠트쿤스트베르크 (Gesamtkunstwerk)</span></a></h3>
<p>건축·인테리어·가구·직물·조명까지 하나의 통합된 예술 작품으로 만들겠다는 이념. 바우하우스의 데사우 캠퍼스 자체가 이 개념의 총체적 실험이었다.</p>
<h2 id="주요-인물과-작품" tabindex="-1"><a class="header-anchor" href="#주요-인물과-작품"><span>주요 인물과 작품</span></a></h2>
<h3 id="발터-그로피우스-walter-gropius" tabindex="-1"><a class="header-anchor" href="#발터-그로피우스-walter-gropius"><span>발터 그로피우스 (Walter Gropius)</span></a></h3>
<p>창립자. 바우하우스 데사우 건물(1926)은 그의 대표작이자 현대 건축의 기념비다. 유리 커튼월, 기능에 따른 공간 분리, 장식의 배제가 특징이다.</p>
<h3 id="마르셀-브로이어-marcel-breuer-와-바실리-체어" tabindex="-1"><a class="header-anchor" href="#마르셀-브로이어-marcel-breuer-와-바실리-체어"><span>마르셀 브로이어 (Marcel Breuer)와 바실리 체어</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/2/2e/Wassilychair.jpg" alt="바실리 체어 (Marcel Breuer, 1927)"></p>
<p>바우하우스 가구의 가장 상징적인 작품. 마르셀 브로이어가 1927년 설계한 강관(tubular steel) 의자로, 자전거 핸들바에서 소재 영감을 얻었다. 화가 바실리 칸딘스키에게 헌정했기에 &quot;바실리 체어&quot;로 불린다. 가죽과 강관의 조합은 당시 혁명적이었으며, 이후 모든 강관 가구 디자인의 원형이 되었다.</p>
<h3 id="허버트-바이어-herbert-bayer-와-타이포그래피" tabindex="-1"><a class="header-anchor" href="#허버트-바이어-herbert-bayer-와-타이포그래피"><span>허버트 바이어 (Herbert Bayer)와 타이포그래피</span></a></h3>
<p>바우하우스의 타이포그래피는 디자인 역사에서 별도로 다룰 만한 혁명이었다. 바이어는 1925년 &quot;유니버살(Universal)&quot; 서체를 개발했는데, 대문자를 완전히 폐기하고 소문자만으로 구성된 기하학적 알파벳이었다. &quot;왜 두 가지 알파벳 체계(대문자·소문자)가 필요한가?&quot;라는 질문에서 출발한 급진적 실험이었다. 바우하우스의 모든 공식 문서와 출판물은 이 원칙에 따라 전부 소문자로 작성되었다.</p>
<h3 id="바실리-칸딘스키-wassily-kandinsky" tabindex="-1"><a class="header-anchor" href="#바실리-칸딘스키-wassily-kandinsky"><span>바실리 칸딘스키 (Wassily Kandinsky)</span></a></h3>
<p>&quot;예술의 정신적 측면(Concerning the Spiritual in Art)&quot;의 저자이자 추상미술의 선구자. 바우하우스에서 색채론과 기초 조형을 가르쳤다. 그의 색채·형태 이론(원=파랑, 삼각형=노랑, 사각형=빨강)은 바우하우스 시각 언어의 토대가 되었다.</p>
<h3 id="파울-클레-paul-klee" tabindex="-1"><a class="header-anchor" href="#파울-클레-paul-klee"><span>파울 클레 (Paul Klee)</span></a></h3>
<p>화가이자 이론가. &quot;조형적 사고(Pädagogisches Skizzenbuch)&quot;를 통해 점·선·면·색의 관계를 체계화했다. 그의 교수법은 오늘날 모든 디자인 기초 교육의 뼈대다.</p>
<h3 id="미스-반-데어-로에-mies-van-der-rohe" tabindex="-1"><a class="header-anchor" href="#미스-반-데어-로에-mies-van-der-rohe"><span>미스 반 데어 로에 (Mies van der Rohe)</span></a></h3>
<p>바우하우스 마지막 교장. &quot;Less is More&quot;의 주창자. 바르셀로나 파빌리온(1929)과 바르셀로나 체어는 바우하우스 원칙의 정점이자, 이후 미니멀리즘 건축·인테리어의 기준이 되었다.</p>
<h2 id="바우하우스가-남긴-것" tabindex="-1"><a class="header-anchor" href="#바우하우스가-남긴-것"><span>바우하우스가 남긴 것</span></a></h2>
