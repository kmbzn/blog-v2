<h2 id="_1-개요" tabindex="-1"><a class="header-anchor" href="#_1-개요"><span>1. 개요</span></a></h2>
<p><strong>김형준</strong>은 대한민국의 대학생으로, <a href="">경희대학교 국제캠퍼스</a> 산업공학과를 졸업 유예 중이다. 현재는 <strong>기업 취업에 실패한 상태</strong>로 버거 프랜차이즈 아르바이트를 전전하고 있으며, 법률 전문가를 목표로 <a href="">LEET(법학적성시험)</a>을 준비하고 있다.</p>
<ul>
<li><a href="">페페(개구리), 라이즈 원빈, 승한, 투모로우바이투게더 연준, 비투비 육성재, 방탄소년단 지민</a>을 닮은 외모를 가지고 있다.</li>
</ul>
<h2 id="_2-성적-지향" tabindex="-1"><a class="header-anchor" href="#_2-성적-지향"><span>2. 성적 지향</span></a></h2>
<p>스스로 <strong><a href="">동성애자</a>임을 밝힌 인물</strong>로, 간헐적으로 여성과의 관계를 상상하며 단체 채팅방에 자신의 성적 취향을 수시로 밝히곤 한다. 따라서 <strong>양성애자</strong>로 간주하는 게 사실상 정확하다. 하지만 <strong>실질적인 성 경험은 전혀 없으며</strong>, 공식적으로 <strong>2025년 6월 현재까지 모태솔로</strong> 상태이다. 최근에는 <strong>같은 부대 후임 출신인 <a href="">황주현</a></strong>(중앙대학교 기계공학부 20)과의 <strong>동성 연애 관계</strong>가 알려져 주목받고 있다.</p>
<h2 id="_3-병역" tabindex="-1"><a class="header-anchor" href="#_3-병역"><span>3. 병역</span></a></h2>
<p>공군 제35비행전대에서 <strong>작전과 작전병으로 복무</strong>, <strong>병장으로 전역</strong>하였다.</p>
<p>아주 <strong>파란만장한 21개월</strong>을 보냈다. 특히 <strong>257대대장, 35작전과장 등 간부들과의 친분</strong>을 과시하며 위계 내에서의 관계 형성에 집착하는 모습을 보였으며, 이로 인해 특유의 존재감을 나타냈다.</p>
<p>군 생활 초반에는 <strong>6개월간 후임병이 들어오지 않는 특이한 공백기</strong>를 겪었고, 이후 들어온 <strong>831, 834, 835기 후임들이 상당한 폐급 수준</strong>이라 전우들과의 관계 형성에 실패해 사실상 허수아비나 다를 바 없는 역할 부재 상태가 되었다. 이로 인해 김형준은 <strong>선임으로서도 후임으로서도 전혀 인정을 받지 못하는 애매한 위치</strong>에 놓이게 되었으며, 해당 상태는 <strong>전역할 때까지 지속</strong>되었다.</p>
<p>여기에 더해, <strong>코로나바이러스-19 전국 대유행 사태</strong>로 <strong>총 121일의 휴가와 117일의 격리 기간</strong>을 포함해, <strong>실제 근무일수 자체가 매우 적었다</strong>는 점에서 전문성 부족은 물론이고 <strong>부실 복무 논란</strong>까지 제기되는 상황이다.</p>
<h2 id="_4-진로" tabindex="-1"><a class="header-anchor" href="#_4-진로"><span>4. 진로</span></a></h2>
<p>전공과는 무관하게 <strong>법조계 및 정계 진출을 목표로 LEET(법학적성시험)를 준비 중</strong>이다. 법률 전문가라는 새로운 진로를 설정하고 장기적으로는 정치인으로의 재도약을 노리는 상황이다. 하지만 현실적으로 많은 어려움이 예상되고 있다.</p>
<h2 id="_5-정치-및-사회-경험" tabindex="-1"><a class="header-anchor" href="#_5-정치-및-사회-경험"><span>5. 정치 및 사회 경험</span></a></h2>
<ul>
<li>
<p><strong><a href="">이재명</a> 대통령</strong>, <a href="">윤석열</a> 전 대통령 <a href="">한동훈</a> 전 대표, <a href="">안철수</a> 국민의힘 의원, <a href="">허경영</a> 명예총재 등 다수의 주요 정치인들과 함께 촬영한 사진을 보유하고 있으며, 정치에 대한 관심이 높은 인물로 알려져 있다.<br>
다만, 이러한 사진 대부분은 <strong>실질적인 교류나 친분에 기반했다기보다는</strong>, <strong>선거 유세 현장에 난입해 사실상 일방적으로 셀카를 촬영한 것</strong>에 가깝다.</p>
</li>
<li>
<p>2025년 6월 2일, <strong>서울 신논현역 인근 유세 현장에 직접 방문하여 국민의힘 김문수 전 대선후보와 사진을 촬영</strong>한 바 있으며,<br>
<strong><a href="">김용태</a> 비상대책위원장과는 개인적인 교류가 있는 것으로 알려져 있다.</strong></p>
</li>
<li>
<p>과거 <strong>국회 내 국민의힘 의원실에서 무보수 사무 보조 활동</strong>을 수행하였고,<br>
<strong>연탄 나눔 봉사 활동에도 직접 참여</strong>하였다. 해당 활동은 언론 보도를 통해 외부에도 알려졌으며,<br>
<strong>관련 뉴스 기사에 김형준 본인의 모습이 노출된 바 있다.</strong></p>
</li>
<li>
<p>정치적 성향은 <strong>극우 성향에 가깝다는 평가</strong>를 받고 있으며,<br>
특히 <strong>이재명 대통령에 대해서는 공개적으로 비판적이고 적대적인 시각</strong>을 유지해 왔다.<br>
더불어, <strong>대한민국의 주요 선거에서 부정이 발생했다고 믿는 이른바 <a href="">부정선거 음모론</a>을 진지하게 신봉하는 인물</strong>로도 알려져 있다.</p>
</li>
</ul>
<h2 id="_6-경제-활동-및-자산" tabindex="-1"><a class="header-anchor" href="#_6-경제-활동-및-자산"><span>6. 경제 활동 및 자산</span></a></h2>
<ul>
<li>
<p>과거 <strong>버거킹 문정점에서 아르바이트</strong>를 하던 중, <strong><a href="">산업재해</a>로 토마토를 써는 기계에 손가락이 절단되는 사고</strong>를 겪었다.<br>
이 사고로 인해 <strong>약 600만 원의 산재 보상금</strong>을 지급받았으며, 이후 <strong>자발적 실업 상태임에도 불구하고 실업급여를 수 개월간 수령</strong>하였다. 병원에서 붕대를 감은 상태로 웃는 표정으로 찍은 거울 셀카를 공개하여 자신의 건재함을 과시하였다. 현재는 <strong>동일 매장에 복직</strong>하여 아르바이트를 지속하고 있다.</p>
</li>
<li>
<p>투자 활동 측면에서는, <strong>국내 주식 투자</strong>를 통해 한때 <strong>약 1억 3천만 원 규모의 자산을 형성</strong>한 바 있으나, 이후 <strong>7천만 원 이상의 손실</strong>을 기록하면서 자산 규모가 대폭 감소하였다. 현재는 <strong>6천만 원대 수준의 자산</strong>을 유지하고 있으며, 그럼에도 불구하고 여전히 국내 주식 시장에서 단타 매매를 이어가고 있다.</p>
<p>투자 성향은 <strong>급등주 중심의 단기 집중투자 전략</strong>으로 알려졌으며, <strong>3번 중 1번 꼴의 성공 확률</strong>을 보이는 것으로 전해진다. 다만, <strong>실패한 사례는 공개하지 않으며</strong>, SNS에는 <strong>선별된 성공 사례만 게시</strong>하고, <strong>수익 금액은 흐리거나 의도적으로 길게 가리는 방식으로 처리</strong>해 외부 해석의 여지를 남기는 방식이 반복적으로 관찰된다.</p>
</li>
<li>
<p><strong>코인이나 해외 주식에는 투자하지 않으며</strong>, 투자 대상은 <strong>국내 주식에 한정</strong>된다.</p>
</li>
<li>
<p>아울러, <strong>투자자산운용사 국가공인 자격증</strong>을 취득한 상태이다.</p>
</li>
<li>
<p>소비 성향에 있어서는 <strong><a href="">발렌시아가</a> 등 고가 브랜드의 악세사리 등을 구매하여 주변에 과시하는 행태</strong>가 포착되며, 실제로는 <strong>소장품 수가 극히 제한적인 상태에서 동일 제품만을 반복 착용하는 모습</strong>을 자주 보여주고 있다. 이러한 점은 <strong>과시적 소비 성향과 자산 대비 실질 구매력 간의 괴리</strong>를 드러내는 사례로 언급되기도 한다.</p>
</li>
</ul>
<h2 id="_7-학업-성과" tabindex="-1"><a class="header-anchor" href="#_7-학업-성과"><span>7. 학업 성과</span></a></h2>
<p>고등학교 재학 중 치른 <strong>전국 단위 교육청 주관 모의고사에서 국+수+영+탐 백분위 합산</strong> 99.97%를 기록한 바 있으며, 이를 반복적으로 주위에 언급하는 것을 보아 이에 대한 상당한 자부심이 있는 것으로 보인다.
특히 <strong>서울 배명고등학교 재학 당시 전교 1등을 도맡아 왔으며</strong>, 교내에서 성취를 인정받은 바 있다.</p>
<p>다만, <strong>평균적인 학업 수준이 전국적으로 낮기로 유명한 배명고등학교의 특성상</strong>, <strong>대학수학능력시험에서는 기대에 한참 미치지 못하는 결과</strong>를 받아 소위 말하는 &quot;지방대&quot;에 해당하는 <strong>경희대학교 국제캠퍼스</strong>(19학번)에 &quot;현대판 음서제&quot;로 평가되는 <strong>수시 전형</strong>으로 진학하게 되었다.</p>
<p>이로 인해 <strong>내신 및 모의고사 성적에 비해 실제 대학 진학 결과가 격차가 크다는 점에서</strong>, 주변 지인들 사이에서 <strong>웃음거리로 회자되는 사례</strong>로 남았다.</p>
