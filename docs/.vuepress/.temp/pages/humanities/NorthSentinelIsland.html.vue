<h2 id="보호구역-지정" tabindex="-1"><a class="header-anchor" href="#보호구역-지정"><span>보호구역 지정</span></a></h2>
<ul>
<li>Andaman and Nicobar Islands Protection of Aboriginal Tribes Regulation 1956에 따라,<br>
섬 및 섬 주변 5해리(약 9 km) 이내 접근이 법적으로 금지됨</li>
<li>인도 해군이 섬 주변을 순찰하며 접근을 방지</li>
<li>Indian authorities는 섬 주민이 외부인의 침입을 방어한 경우, 사법 책임을 묻지 않음</li>
</ul>
<h2 id="지형과-생태" tabindex="-1"><a class="header-anchor" href="#지형과-생태"><span>지형과 생태</span></a></h2>
<ul>
<li>
<p>섬은 산호초(coral reefs)에 둘러싸여 있으며,<br>
자연 항구가 거의 없고, 내부는 전면 숲으로 덮여 있음</p>
</li>
<li>
<p>해변을 따라 약 20m 높이의 지형이 이어지며,<br>
섬 중심부는 최대 122m에 이르는 완만한 언덕으로 구성</p>
</li>
<li>
<p>2004년 인도양 지진 이후 지반이 1–2 m 융기,<br>
산호초 일부가 드러나 연결되거나 습지 형태로 변형됨</p>
</li>
<li>
<p>열대우림 환경:</p>
<ul>
<li>큰 나무(예: Bombax ceiba), 인도 멧돼지, 코코넛게 등 서식</li>
<li>조류, 상어, 해거북, 돌고래 등도 관찰된 바 있음</li>
</ul>
</li>
</ul>
<h2 id="역사" tabindex="-1"><a class="header-anchor" href="#역사"><span>역사</span></a></h2>
<h3 id="초기-정보" tabindex="-1"><a class="header-anchor" href="#초기-정보"><span>초기 정보</span></a></h3>
<ul>
<li>온제(Onge)족 등 다른 안다만 지역 부족들은 섬의 존재를 알고 있었음</li>
<li>언어가 달라 실제 접촉은 거의 이루어지지 않았고,<br>
최초 영국 기록으로는 1771년 survey navigation이 있음</li>
</ul>
<h3 id="식민기-접촉-시도" tabindex="-1"><a class="header-anchor" href="#식민기-접촉-시도"><span>식민기 접촉 시도</span></a></h3>
<ul>
<li>1867년 영국 상선 승선 시도(106명 탈출 생존 사례 있음)</li>
<li>1880년 <em>Maurice Vidal Portman</em>이 탐험단 이끌고 상륙,<br>
일부 섬 주민이 포틀 블레어로 데려감 → 병 사망 → 귀환</li>
<li>1883–1887년 간헐적 방문 및 선물 교환</li>
</ul>
<h3 id="독립-이후-상황" tabindex="-1"><a class="header-anchor" href="#독립-이후-상황"><span>독립 이후 상황</span></a></h3>
<ul>
<li>1967년 이후 인도 정부 탐험대의 친선 방문 시도 있었으나,<br>
1975년 이후 공식적인 방문은 제한됨</li>
<li>1977년 및 1981년 선박 좌초 사건 발생</li>
<li>1991년 최초 평화 접촉 시도(코코넛 선물 교환 등),<br>
1997년 이후 공식 접촉 중단됨</li>
<li>2004년 쓰나미 이후에도 섬 주민은 생존</li>
<li>2006년, 불법 어업 어선 2척이 침범해 주민이 인명 피해 (인도인 2명 사망)</li>
<li>2018년 미국인 선교사 John Chau 사망 사건 발생</li>
<li>2025년 3월 미확인 미국인 관광객이 불법 상륙 후 체포됨</li>
</ul>
