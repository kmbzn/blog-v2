<ul>
<li><code v-pre>uvmcopy()</code>: 복사 대신 페이지 공유</li>
<li><code v-pre>usertrap()</code>: COW 페이지 폴트 처리</li>
<li><code v-pre>kfreevm()</code>: 커널 작업에 COW 로직 적용</li>
<li><code v-pre>fork()</code> / <code v-pre>wait()</code> / <code v-pre>exit()</code>: 참조 카운팅 구현</li>
</ul>
</li>
<li><strong>주요 과제:</strong>
<ul>
<li>COW 폴트와 다른 페이지 폴트 구분</li>
<li>페이지 참조 카운터의 적절한 관리</li>
<li>메모리 부족 상황 처리</li>
<li>기존 코드와의 호환성 유지</li>
</ul>
</li>
</ul>
