<h1 id="심볼릭-링크" tabindex="-1"><a class="header-anchor" href="#심볼릭-링크"><span>심볼릭 링크</span></a></h1>
<h2 id="개요-3" tabindex="-1"><a class="header-anchor" href="#개요-3"><span>개요</span></a></h2>
<ul>
<li><strong>문제점: xv6의 경직된 파일 참조</strong>
<ul>
<li>하드 링크만 사용 가능 (inode 기반)</li>
<li>디렉토리를 링크하거나 파일 시스템을 넘나들 수 없음</li>
<li>끊어지거나 유연한 링크를 지원하지 않음</li>
</ul>
</li>
<li><strong>해결책: 심볼릭 링크</strong>
<ul>
<li>대상 경로를 파일 데이터로 저장</li>
<li>존재하지 않는 파일을 가리킬 수 있음</li>
<li>더 유연한 링크 및 참조 가능</li>
<li>시스템 유연성 향상</li>
</ul>
</li>
</ul>
