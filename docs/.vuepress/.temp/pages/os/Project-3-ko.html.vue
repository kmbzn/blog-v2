<h2 id="개요-1" tabindex="-1"><a class="header-anchor" href="#개요-1"><span>개요</span></a></h2>
<ul>
<li><strong>문제점: fork()에서의 비효율적인 메모리 복사</strong>
<ul>
<li>현재 fork()는 부모의 모든 메모리를 즉시 복사함
<ul>
<li>비효율적이고 시간이 많이 소요됨</li>
<li>메모리 부족으로 실패할 수 있음</li>
</ul>
</li>
</ul>
</li>
<li><strong>해결책: 쓰기 시 복사 (Copy-on-Write, COW)</strong>
<ul>
<li>초기에는 부모와 자식 간에 페이지를 공유</li>
<li>페이지가 수정될 때만 복사</li>
</ul>
</li>
</ul>
<h2 id="cow-fork-구조" tabindex="-1"><a class="header-anchor" href="#cow-fork-구조"><span>COW Fork 구조</span></a></h2>
<ul>
<li><strong>초기 상태:</strong>
<ul>
<li>자식의 페이지 테이블이 부모의 물리 페이지를 가리킴</li>
<li>쓰기 접근을 트랩하기 위해 부모의 PTE를 읽기 전용으로 표시</li>
<li>RSW (소프트웨어 예약) 비트를 사용하여 COW 비트를 설정</li>
</ul>
</li>
<li><strong>쓰기 접근:</strong>
<ul>
<li>쓰기 시도 시 <strong>페이지 폴트</strong> 발생</li>
<li>커널이 새로운 물리 페이지를 할당</li>
<li>원본 페이지를 새 페이지로 복사</li>
<li>PTE를 쓰기 권한으로 업데이트</li>
</ul>
</li>
<li><strong>참조 카운팅:</strong>
<ul>
<li>각 페이지를 공유하는 프로세스의 수를 추적</li>
<li>참조 카운트가 0이 될 때만 페이지를 해제</li>
</ul>
</li>
</ul>
<h2 id="구현-과제" tabindex="-1"><a class="header-anchor" href="#구현-과제"><span>구현 과제</span></a></h2>
<ul>
