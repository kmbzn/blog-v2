<li><strong>시스템 콜 인터페이스:</strong>
<ul>
<li><code v-pre>sys_symlink()</code>: (1단계) 심볼릭 링크 파일 생성</li>
</ul>
</li>
<li><strong>수정된 <code v-pre>open()</code></strong>: (2단계) 심볼릭 링크를 직접 따라감
<ul>
<li>상대/절대 경로, 링크 순환 처리</li>
</ul>
</li>
<li><strong>경로 해석:</strong>
<ul>
<li>중첩된 링크에 대한 재귀적 해석</li>
<li>무한 루프 방지를 위한 순환 감지</li>
<li>끊어진 링크에 대한 적절한 오류 처리</li>
</ul>
</li>
</ul>
