<h2 id="레노버-시절-2005–현재" tabindex="-1"><a class="header-anchor" href="#레노버-시절-2005–현재"><span>레노버 시절 (2005–현재)</span></a></h2>
<h3 id="인수" tabindex="-1"><a class="header-anchor" href="#인수"><span>인수</span></a></h3>
<p>2005년 레노버(联想, Lenovo)가 IBM의 PC·노트북 사업부를 <strong>약 17억 5천만 달러</strong>에 인수했다. ThinkPad 브랜드와 설계 팀, 야마토 연구소(大和研究所, 현 레노버 재팬 연구소)까지 모두 이전됐다.</p>
<p>초기에는 &quot;품질이 유지될까&quot;라는 우려가 있었으나, 레노버는 IBM의 엔지니어링 팀과 디자인 언어를 상당 부분 유지했고, 이후 출시된 제품들이 품질을 증명하면서 우려는 사그라들었다.</p>
<h3 id="x1-carbon-현대-thinkpad의-상징" tabindex="-1"><a class="header-anchor" href="#x1-carbon-현대-thinkpad의-상징"><span>X1 Carbon, 현대 ThinkPad의 상징</span></a></h3>
