<p>ThinkPad는 1992년 IBM이 처음 선보인 이후 현재는 레노버(Lenovo)가 제조·판매하는 노트북 컴퓨터 시리즈다. 30년 이상 이어져 온 디자인 언어와 키보드 품질, 그리고 뛰어난 Linux 호환성으로 개발자·엔지니어·파워 유저 집단에서 압도적인 지지를 받는 브랜드다.</p>
<h2 id="ibm-시절-1992–2004" tabindex="-1"><a class="header-anchor" href="#ibm-시절-1992–2004"><span>IBM 시절 (1992–2004)</span></a></h2>
<h3 id="탄생" tabindex="-1"><a class="header-anchor" href="#탄생"><span>탄생</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/6/6e/IBM_ThinkPad_700C.jpg" alt="IBM ThinkPad 700C (1992)"></p>
<p>ThinkPad는 1992년 IBM의 산업 디자이너 <strong>리처드 사퍼</strong>(Richard Sapper)와 <strong>나이토 아리마사(内藤在正)</strong> 팀이 설계해 출시했다. 이름은 IBM 직원들이 아이디어를 메모하던 &quot;Think&quot; 패드에서 유래했다.</p>
<p>초기 컨셉은 일본 가이세키 요리에 사용되는 <strong>검은 도시락 상자</strong>(黒べんとう)에서 영감을 받았다고 알려져 있다. 두껍지 않고 각진 검은 직사각형 바디, 빨간 점 포인팅 디바이스(TrackPoint). 이 두 요소는 이후 ThinkPad의 불변하는 아이덴티티가 되었다.</p>
<h3 id="trackpoint-빨간-점의-탄생" tabindex="-1"><a class="header-anchor" href="#trackpoint-빨간-점의-탄생"><span>TrackPoint, 빨간 점의 탄생</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/3/3a/Mouse_pointer.svg" alt="TrackPoint on a ThinkPad keyboard"></p>
<p>ThinkPad의 상징인 <strong>TrackPoint</strong>(트랙포인트)는 키보드 중앙부에 위치한 등방성 감압 조이스틱이다. 1984년 IBM 연구원 <strong>테드 셀커</strong>(Ted Selker)가 발명했다.</p>
<p>트랙포인트의 핵심 가치는 <strong>손이 키보드 홈 포지션에서 벗어나지 않고도 포인터를 조작할 수 있다</strong>는 것이다. 마우스 없이 키보드와 트랙포인트만으로 모든 작업을 완결할 수 있어, 적응한 사용자들은 &quot;트랙패드로는 돌아갈 수 없다&quot;고 말한다.</p>
<h3 id="thinkpad-701c-나비-키보드-1995" tabindex="-1"><a class="header-anchor" href="#thinkpad-701c-나비-키보드-1995"><span>ThinkPad 701C, 나비 키보드 (1995)</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/a/a0/IBM_ThinkPad_701C.jpg" alt="IBM ThinkPad 701C butterfly keyboard"></p>
<p>1995년 출시된 ThinkPad 701C는 **&quot;나비 키보드(Butterfly Keyboard)&quot;**로 역사에 남는다. 공식 명칭은 <strong>TrackWrite Keyboard</strong>로, 닫혀 있을 때는 화면 크기에 맞게 접혀 있고, 열면 두 개의 날개처럼 펼쳐지며 풀사이즈 키보드가 완성되는 구조다.</p>
<p>이 키보드는 순수한 기계 공학적 성취로, <strong>뉴욕 현대미술관(MoMA) 영구 소장품</strong>으로 등재되어 있다. 실용성보다 아이디어의 완성도 면에서 컴퓨터 디자인 역사에 남는 작품이다.</p>
