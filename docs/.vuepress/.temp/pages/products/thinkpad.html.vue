<li>Intel Evo 플랫폼 기반 고성능</li>
<li>OLED 디스플레이 옵션 (최근 세대)</li>
<li>Thunderbolt 4, HDMI, USB-A 포트 구성</li>
<li><strong>개발자·IT 종사자 사이에서 사실상 &quot;프리미엄 업무 노트북&quot;의 대명사</strong></li>
</ul>
