<h3 id="쫀득한-키감의-비밀" tabindex="-1"><a class="header-anchor" href="#쫀득한-키감의-비밀"><span>&quot;쫀득한&quot; 키감의 비밀</span></a></h3>
<p>ThinkPad 키보드를 쓴 사람들이 자주 쓰는 표현이 **&quot;쫀득하다&quot;**는 것이다. 이는 다음의 조합에서 나온다:</p>
<ol>
<li><strong>스카이라인(Scissor-switch) 구조</strong>: 가위 형태의 서스펜션이 키캡 네 모서리를 균일하게 지지</li>
<li><strong>명확한 트리거 포인트</strong>: 누를 때 특정 지점에서 분명한 저항 → 촉각 피드백</li>
<li><strong>바닥 감쇠</strong>: 키가 바닥에 닿을 때 과도한 충격 없이 부드럽게 멈춤</li>
</ol>
<p>비교 대상으로 흔히 나오는 MacBook의 나비 키보드(2016~2019)는 여행거리가 0.4mm에 불과해 &quot;타이핑감이 없다&quot;는 악평을 받았고, 이후 Apple도 다시 scissor-switch로 돌아갔다.</p>
<h3 id="구형-7열-키보드-pre-2012" tabindex="-1"><a class="header-anchor" href="#구형-7열-키보드-pre-2012"><span>구형 7열 키보드 (Pre-2012)</span></a></h3>
<p><img src="https://upload.wikimedia.org/wikipedia/commons/e/e9/Thinkpad_t410.jpg" alt="ThinkPad T420 classic 7-row keyboard"></p>
<p>2012년 이전 ThinkPad(T420, T410, X220 등)의 키보드는 <strong>7열 배열</strong>을 사용했다. 이 키보드는 ThinkPad 역사상 가장 높은 평가를 받으며, 일부 매니아들은 이후 모델(아일랜드 키보드)보다 구형 키보드를 선호해 <strong>T430에 T420 키보드를 이식(mod)하는 커스터마이징</strong>을 즐기기도 했다.</p>
<h2 id="ubuntu와의-조합" tabindex="-1"><a class="header-anchor" href="#ubuntu와의-조합"><span>Ubuntu와의 조합</span></a></h2>
<p>ThinkPad는 Linux, 특히 Ubuntu와의 호환성 면에서 최상위 노트북이다.</p>
<h3 id="공식-인증" tabindex="-1"><a class="header-anchor" href="#공식-인증"><span>공식 인증</span></a></h3>
<p>레노버와 캐노니컬(Canonical, Ubuntu 개발사)은 공식 파트너십을 맺고 있으며, 다수의 ThinkPad 모델이 <strong>Ubuntu 공식 인증</strong>(Certified Hardware)을 받았다.</p>
<p>Ubuntu 공식 사이트의 하드웨어 인증 목록에서 ThinkPad 라인의 비중은 압도적으로 높다.</p>
