<li>High Cohesion: Classк°Җ н•ҳлӮҳмқҳ кё°лҠҘ мҳҒм—ӯм—җм„ң м ҒлӢ№н•ң мұ…мһ„мқ„ к°Җм§Җл©°, мһ‘м—…мқ„ мҲҳн–үн•ҳкё° мң„н•ҙ (лӢӨлҘё) classesмҷҖ нҳ‘л Ҙ</li>
<li>High cohesionмқҳ мһҘм җ
<ul>
<li>Classes мң м§ҖліҙмҲҳ мҡ©мқҙ</li>
<li>мқҙн•ҙ мҡ©мқҙ</li>
<li>мў…мў… low coupling м§Җмӣҗ</li>
<li>м„ёл¶„нҷ”лҗң мұ…мһ„мңјлЎң мһ¬мӮ¬мҡ©м„ұ м§Җмӣҗ</li>
</ul>
</li>
<li>кІҪн—ҳ лІ•м№ҷ (Rule of thumb): high cohesionмқ„ к°Җм§„ classлҠ” мғҒлҢҖм ҒмңјлЎң м ҒмқҖ мҲҳмқҳ кі лҸ„лЎң м—°кҙҖлҗң кё°лҠҘмқҳ methodsлҘј к°Җм§Җл©°, л„Ҳл¬ҙ л§ҺмқҖ мһ‘м—…мқ„ мҲҳн–үн•ҳм§Җ м•ҠмқҢ</li>
</ul>
