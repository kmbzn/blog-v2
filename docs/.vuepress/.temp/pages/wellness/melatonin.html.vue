<h2 id="생리적-역할" tabindex="-1"><a class="header-anchor" href="#생리적-역할"><span>생리적 역할</span></a></h2>
<p>멜라토닌은 뇌의 송과선(pineal gland)에서 합성·분비되는 호르몬으로, 일주기 리듬(circadian rhythm)의 주요 조절자다. 분비는 빛에 의해 억제되며, 어두운 환경에서 저녁 9–10시경부터 증가하여 새벽 2–4시경 최고치에 달한다.</p>
<p>MT1 및 MT2 수용체에 작용하여 수면 개시를 유도하고 체온을 낮추며, 일주기 시계를 외부 환경과 동기화한다.</p>
