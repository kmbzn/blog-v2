<h2 id="작용-기전" tabindex="-1"><a class="header-anchor" href="#작용-기전"><span>작용 기전</span></a></h2>
<p>설트랄린은 시냅스 전 뉴런의 세로토닌(Serotonin) 재흡수 수송체(SERT)를 선택적으로 억제하여, 시냅스 간극 내 세로토닌 농도를 증가시킨다. 도파민 및 노르에피네프린 재흡수에 대한 영향은 매우 미미하다.</p>
<p>항우울 효과는 투여 시작 후 수 일~수 주의 지연 기간 후 나타나며, 이는 신경 가소성(neuroplasticity) 변화와 관련된 것으로 추정된다.</p>
